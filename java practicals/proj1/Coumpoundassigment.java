package hello;

public class Coumpoundassigment {
	public static void main(String[] args) {
		int a=40;
        a += 5;
        System.out.println("After compounding +=5: " + a);
        a -= 2;
        System.out.println("After compounding -=2: " + a);
        a *= 3;
        System.out.println("After compounding *=3: " + a);
        a /= 2;
        System.out.println("After compounding /=2: " + a);
        a %= 4;
        System.out.println("After compounding %=4: " + a);			
		
	}
}
